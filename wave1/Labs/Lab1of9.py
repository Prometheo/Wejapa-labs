text = "hello, my name is boluwatife, and this is a sample text, again, call me boluwatife"
#replace method
print(text.replace('boluwatife', 'precious'))
#case method, capitalize first letter
print(text.capitalize())
#make all uppercase
print(text.upper())
#splits the text into words.
print(text.split(' '))